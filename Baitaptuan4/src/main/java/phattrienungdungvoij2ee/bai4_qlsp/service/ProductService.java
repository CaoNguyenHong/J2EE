package phattrienungdungvoij2ee.bai4_qlsp.service;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.nio.file.StandardCopyOption;
import java.util.ArrayList;
import java.util.List;
import java.util.UUID;

import org.springframework.stereotype.Service;
import org.springframework.web.multipart.MultipartFile;

import phattrienungdungvoij2ee.bai4_qlsp.model.Product;

@Service
public class ProductService {
	List<Product> listProduct = new ArrayList<>();
	
	public List<Product> getAll() {
		return listProduct;
	}
	
	public Product get(int id) {
		return listProduct.stream()
				.filter(p -> p.getId() == id)
				.findFirst()
				.orElse(null);
	}
	
	public void add(Product newProduct) {
		int maxId = listProduct.stream()
				.mapToInt(Product::getId)
				.max()
				.orElse(0);
		newProduct.setId(maxId + 1);
		listProduct.add(newProduct);
	}
	
	public void update(Product editProduct) {
		Product find = get(editProduct.getId());
		if (find != null) {
			find.setPrice(editProduct.getPrice());
			find.setName(editProduct.getName());
			if (editProduct.getImage() != null) find.setImage(editProduct.getImage());
			if (editProduct.getCategory() != null) find.setCategory(editProduct.getCategory());
		}
	}

	public void delete(int id) {
		listProduct.removeIf(p -> p.getId() == id);
	}
	
	public void updateImage(Product newProduct, MultipartFile imageProduct) {
		if (!imageProduct.isEmpty()) {
			String contentType = imageProduct.getContentType();
			if (contentType != null && !contentType.startsWith("image/")) {
				throw new IllegalArgumentException("Tệp tải lên không phải là hình ảnh!");
			}
			try {
				// Lấy đường dẫn tuyệt đối từ thư mục project
				String projectRoot = System.getProperty("user.dir");
				Path dirImages = Paths.get(projectRoot, "src/main/resources/static/images");
				if (!Files.exists(dirImages)) {
					Files.createDirectories(dirImages);
				}
				String originalFilename = imageProduct.getOriginalFilename();
				// Thay thế khoảng trắng bằng dấu gạch dưới để tránh lỗi URL
				if (originalFilename != null) {
					originalFilename = originalFilename.replaceAll("\\s+", "_");
				}
				String newFileName = UUID.randomUUID() + "_" + originalFilename;
				Path pathFileUpload = dirImages.resolve(newFileName);
				Files.copy(imageProduct.getInputStream(), pathFileUpload, StandardCopyOption.REPLACE_EXISTING);
				newProduct.setImage(newFileName);
			} catch (IOException e) {
				e.printStackTrace();
				throw new RuntimeException("Lỗi khi lưu hình ảnh: " + e.getMessage(), e);
			}
		}
	}
}
